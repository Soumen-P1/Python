num1 = 10
num2 = 2

# arithmetic operator
print("num1 + num2 is ", num1+num2)
print("num1 - num2 is ", num1-num2)
print("num1 * num2 is ", num1*num2)
print("num1 / num2 is ", num1/num2)
print("num1 ** num2 is ", num1**num2)
print("num1 // num2 is ", num1//num2)
print("num1 % num2 is ", num1 % num2)

# assignment operator
a = 4
a += 2
print(a)

# comparison operator
x = 4
y = 3
z = 4
print(x > y)
print(x < y)
print(x == y)
print(x != y)

# logical operators
print(x == z and x < y)
print(x == z or x < y)
print(not(True))

